#include <array>
#include <math.h>

#include "picosystem.hpp"
#include "include.hpp"

using namespace picosystem;

extern const color_t house[5400]; // see below for data
buffer_t house_image(120, 45, (color_t *)house);

v2_t wind = {25.0f, 0.0f};
bool lightning = true;

struct rain_particle_t : public particle_t {
  v2_t dir;

  void create() {
    pos = {rndf(-80, 200), rndf(-20, 0)};
    dir = {rndf(-0.5f, 0.5f), rndf(1.0f, 2.0f)};
  }

  void update(float elapsed) {
    static v2_t gravity = {0, 40.0f};

    // apply gravity to the direction vector
    dir += gravity * elapsed;

    // apply drift to particles, ensures smoke spreads and rises over time
    pos += dir * elapsed;

    pos.x += wind.x * elapsed;

    // check if collided with house

    // calculate coordinates of this raindrop within the house graphic
    int32_t cx = pos.x;
    int32_t cy = pos.y - 66;

    if(cx >= 0 && cx < 120 && cy >= 0 && cy < 45) {
      // potential collision

      if(a(*house_image.p(cx, cy))) {
        // compare against alpha channel of pixel in house image

        pos -= dir * elapsed;
        dir.y *= rndf(0.0f, 0.1f);
        dir.y *= -1.0f;
      }
    }

    if(pos.y > 96) {
      pos.y = 96;
      dir.y *= rndf(0.0f, 0.1f);
      dir.y *= -1.0f;
    }
  }

  void draw() {
    // draw the particle
    pixel(pos.x, pos.y);
  }

  static void update_obstacle() {

    wind.rotate(rndf(-1.0f, 2.5f));
  }
};

emitter_t<rain_particle_t> rain(2500, 3000);

void init() {
}


void update(uint32_t tick) {
  if(pressed(Y)) {
    rain.reset();
  }

  if(button(RIGHT)) {
    wind.rotate(-5);
  }

  if(button(LEFT)) {
    wind.rotate(5);
  }

  if(pressed(B)) {
    lightning = !lightning;
  }

  if(pressed(X)) {
    if(rain.max_count > 500) {
      rain.max_count -= 500;
    } else {
      rain.max_count = 2500;
    }

    rain.reset();
  }

  rain.update();
}

void LIGHTNING(color_t *ps, int32_t so, int32_t ss, color_t *pd, uint32_t c) {
  while(c--) {
    uint32_t dr = (*pd & 0x000f) >>  0;
    uint32_t dg = (*pd & 0xf000) >> 12;
    uint32_t db = (*pd & 0x0f00) >>  8;

    if(dr + dg + db < 16) {
      *pd = 0xffff;
    }else{
      *pd = 0x00f0;
    }
    pd++;
  }
}


void draw(uint32_t tick) {
  // clear background with semi transparent colour - this gives us motion
  // blur/streaking on the rain drops
  pen(1, 0, 2, 2);
  clear();

  // draw house image, use MASK blend mode as image only contains fully opaque
  // or transparent pixels, nothing in between
  blend(MASK);
  blit(&house_image, 0, 0, 120, 45, 0, 66);

  // reset blend mode to default and draw particles
  blend();
  pen(6, 6, 8, 8);
  rain.draw();

  // on average 1 in every 100 frames will be a "lightning strike"
  if(lightning && rnd(0, 100) == 0) {
    // custom blend mode to draw over entire screen with lightning effect
    blend(LIGHTNING); frect(0, 0, 120, 120); blend();
  }

  // draw wind arrow
  pen(15, 15, 15, 8);
  camera(-100, -30);
  v2_t wv = wind;
  wv.normalise();
  wv *= 12.0f;
  line(0, 0, wv.x, wv.y);
  v2_t ah = wv / 2;
  ah.rotate(160);
  line(wv.x, wv.y, wv.x + ah.x, wv.y + ah.y);
  ah.rotate(40);
  line(wv.x, wv.y, wv.x + ah.x, wv.y + ah.y);
  camera();


  // draw title
  pen(15, 15, 15, 12); frect(0, 0, 240, 9);
  pen(5, 7, 9); text("RAIN", 2, 2);

  // draw menu / state
  pen(15, 15, 15);
  cursor(2, 12);
  text(std::string("LIGHTNING: ") + (lightning ? "YES": "NO") + "   \\pen4cffB");
  text(std::string("WIND: ") + str(wind.angle(), 0) + "'   \\pen4cffL/R");
  text(std::string("COUNT: ") + str(rain.max_count) + "   \\pen4cffX");
  text(std::string("RESET   \\pen4cffY"));

  // draw fps
  pen(4, 4, 4, 8); frect(0, 111, 120, 9);
  pen(15, 15, 15); text("FPS: " + str(stats.fps), 2, 113);

}

